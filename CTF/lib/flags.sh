#!/usr/bin/env bash
# =============================================================================
#  CoLab CTF — flag store & verification
#  Flag format: CoLab{lowercase_snake_case}
#
#  SECURITY MODEL
#   • The repo stores SHA-256 HASHES ONLY (flags/flag-hashes.txt).
#   • Plaintext flags are generated at seed time into ctf/state/flags.plain,
#     which is gitignored and chmod 600. Provisioning plants from there.
#   • Submissions are verified by hashing the input and comparing to the store.
#     Plaintext never touches version control.
# =============================================================================

flags_store()  { printf '%s/flags/flag-hashes.txt' "$CTF_HOME"; }
flags_plainf() { printf '%s/state/flags.plain'     "$CTF_HOME"; }

# ---- SHA-256 of an exact string (no trailing newline) -----------------------
flags_sha256() {
  local input="$1"
  if command -v sha256sum >/dev/null 2>&1; then
    printf '%s' "$input" | sha256sum | awk '{print $1}'
  elif command -v shasum >/dev/null 2>&1; then
    printf '%s' "$input" | shasum -a 256 | awk '{print $1}'
  elif command -v openssl >/dev/null 2>&1; then
    printf '%s' "$input" | openssl dgst -sha256 | awk '{print $NF}'
  else
    echo "ERR: no sha256 tool (need sha256sum/shasum/openssl)" >&2
    return 1
  fi
}

flags_rand() {
  if [[ -r /dev/urandom ]]; then
    head -c 8 /dev/urandom | od -An -tx1 | tr -d ' \n' | cut -c1-8
  else
    printf '%04x%04x' "$((RANDOM))" "$((RANDOM))"
  fi
}

# ---- Seed: generate plaintext (gitignored) + recompute the hash store --------
# Idempotent; pass --force to regenerate (invalidates already-captured flags).
flags_seed() {
  local force="${1:-}"
  local plain store id slug flag hash
  plain="$(flags_plainf)"; store="$(flags_store)"
  mkdir -p "$CTF_HOME/state" "$CTF_HOME/flags"

  if [[ -s "$plain" && "$force" != "--force" ]]; then
    return 0
  fi

  : > "$plain"; chmod 600 "$plain" 2>/dev/null || true
  {
    echo "# CoLab CTF — flag hash store (SHA-256 only; NO plaintext)."
    echo "# Regenerated by flags_seed(). Format: <challenge_id>\\t<sha256>"
  } > "$store"

  for id in "${CHALLENGE_ORDER[@]}"; do
    slug="${CH_SLUG[$id]:-flag}"
    flag="CoLab{${slug}_$(flags_rand)}"
    hash="$(flags_sha256 "$flag")"
    printf '%s\t%s\n' "$id" "$flag" >> "$plain"
    printf '%s\t%s\n' "$id" "$hash" >> "$store"
  done
}

# Plaintext flag for a challenge (used ONLY by provisioning to plant the target).
flags_plaintext() {
  local id="$1"
  [[ -s "$(flags_plainf)" ]] || return 1
  awk -F'\t' -v id="$id" '$1==id{print $2; exit}' "$(flags_plainf)"
}

# Stored hash for a challenge.
flags_hash() {
  local id="$1"
  [[ -s "$(flags_store)" ]] || return 1
  awk -F'\t' -v id="$id" '$1==id{print $2; exit}' "$(flags_store)"
}

# Verify a submitted flag against the stored hash (hash-compare only).
flags_verify() {
  local id="$1" sub="$2" want got
  sub="$(printf '%s' "$sub" | sed -e 's/^[[:space:]]*//' -e 's/[[:space:]]*$//')"
  [[ -n "$sub" ]] || return 1
  want="$(flags_hash "$id")" || return 1
  got="$(flags_sha256 "$sub")" || return 1
  [[ -n "$want" && "$got" == "$want" ]]
}
